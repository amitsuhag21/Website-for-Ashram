                    <h2>Mission of Oshodhara </h2>

                    <p>
                        <br /> ●
                        <strong> Oshodhara is committed to take the cycle of &#39;Dharma Chakra Pravartan&#39; initiated by Osho beyond the barrier of time. Osho&rsquo;s dream of &nbsp;creating &nbsp;10,000 Buddhas on this Earth is being fulfilled by Oshodhara. Oshodhara is also committed to establish Scientific Spirituality based on &#39;Spiritual Science&#39; given by Sadguru Osho Siddharth Aulia.&nbsp;</strong>
                        <br />
                        <br /> Having converted spirituality into a science, Oshodhara is based on 4 pillars namely&nbsp;</p>

                    <p><strong>1. Meditation &nbsp;<br />
2. Sakshi &nbsp;<br />
3. Samadhi&nbsp;<br />
4. Sumiran</strong>&nbsp;
